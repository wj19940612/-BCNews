public class MyIntegral {

    /**
     * rate : 0
     * integral : 0
     */

    private int rate;
    private double integral;

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public double getIntegral() {
        return integral;
    }

    public void setIntegral(double integral) {
        this.integral = integral;
    }

    @Override
    public String toString() {
        return "MyIntegral{" +
                "rate=" + rate +
                ", integral=" + integral +
                '}';
    }
}
